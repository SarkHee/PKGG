const LanguageContext = createContext({
  lang: 'ko',
  t: (key) => key,
  switchLang: () => {},
});

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState('ko');

  useEffect(() => {
    const saved = localStorage.getItem('pkgg_lang');
    if (saved && LANGS.includes(saved)) setLang(saved);
  }, []);

  const t = (key) =>
    translations[lang]?.[key] ?? translations['ko']?.[key] ?? key;

  const switchLang = (newLang) => {
    if (!LANGS.includes(newLang)) return;
    setLang(newLang);
    localStorage.setItem('pkgg_lang', newLang);
  };

  return (
    <LanguageContext.Provider value={{ lang, t, switchLang }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useT() {
  return useContext(LanguageContext);
}
